const response = 2 * 4; 

console.log(`The response is ${response}`);

let responseDoubled = 2 * response;

console.log(`Twice the response is ${responseDoubled}`);

const name = 'Josh';
const names = [];
while (responseDoubled--){
  names.push(name);
}

console.log(`my name that many times is ${names.join()}`)